package com.literals.java;

public class Literals {
    public static void main(String[] args) {


        // literals are fixed values in our code that is directly assigned to datatypes

        byte value;
        value = 23;// lterals

        short sh;
        sh = 2333;// lterals

        int in;
        in = 344233;// lterals

        long ln;
        ln = 2242244;// lterals

        float fl;
        fl = 344.4f;// lterals

        double db;
        db = 123.34;// lterals

        char ch;
        ch = 'v';


        boolean booleanIsTrue;
        booleanIsTrue = true;

        System.out.println("the byte value is:" + value);
        System.out.println("the short value is:" + sh);
        System.out.println("the int value is:" + in);
        System.out.println("the long value is:" + ln);
        System.out.println("the float value is:" + fl);
        System.out.println("the double value is:" + db);
        System.out.println("the char value is:" + ch);
        System.out.println("the boolen value is:" + booleanIsTrue);
    }
}
